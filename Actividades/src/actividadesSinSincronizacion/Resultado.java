package actividadesSinSincronizacion;

public class Resultado {

	private double importeH;
	private double importeR;
	private double importeL;
	
	
	
	public Resultado(double importeH, double importeR, double importeL) {
		this.importeH = importeH;
		this.importeR = importeR;
		this.importeL= importeL; 
	}
	public double getImporteH() {
		return importeH;
	}
	public double getImporteR() {
		return importeR;
	}
	public double getImporteL() {
		return importeL;
	}
	
	
}
